package com.springbootlearning.learningspringboot3;

import static org.assertj.core.api.Assertions.*;
import static org.hamcrest.Matchers.*;
import static org.mockito.Mockito.*;
import static org.springframework.security.test.web.servlet.request.SecurityMockMvcRequestPostProcessors.*;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.*;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.*;

import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.web.servlet.WebMvcTest;
import org.springframework.boot.test.mock.mockito.MockBean;
import org.springframework.security.test.context.support.WithMockUser;
import org.springframework.test.web.servlet.MockMvc;

@WebMvcTest(controllers = HomeController.class)
public class HomeControllerTest {

	@Autowired
	MockMvc mvc;

	@MockBean
	VideoService videoService;

	@Test
	@WithMockUser
	void indexPageHasSeveralHtmlForms() throws Exception {
		// @formatter:off
		String html = mvc.perform(
					get("/"))
				.andExpect(status().isOk())
				.andExpect(
						content().string(
								containsString("Username: user")))
				.andExpect(
						content().string(
								containsString("Authorities: [ROLE_USER]")))
				.andReturn()
				.getResponse()
				.getContentAsString();

		assertThat(html).contains(
				"<form action=\"/logout\"", 
				"<form action=\"/search\"",
				"<form action=\"/new-video\"");
		// @formatter:on
	}

	@Test
	@WithMockUser
	void postNewVideoShouldWork() throws Exception {
		// @formatter:off
		mvc.perform(
					post("/new-video")
					.param("name", "new video")
					.param("description", "new desc")
					.with(csrf()))
				.andExpect(redirectedUrl("/"));

		verify(videoService).create(
				new NewVideo("new video", "new desc"), 
				"user");
		// @formatter:on
	}

	@Test
	@WithMockUser
	void postSearchVideoShouldWork() throws Exception {
		// @formatter:off
		String html = mvc.perform(
					post("/search")
					.param("value", "video")
					.with(csrf()))
			.andExpect(status().isOk())
			.andReturn()
			.getResponse()
			.getContentAsString();
		// @formatter:on
		// TODO: asserts here
		System.out.println("HTML = " + html);
	}

}
